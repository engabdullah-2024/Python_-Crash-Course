# Demonstrates that a script executes top-to-bottom and can print calculation results.

print("Let's do some quick math.")

sum_result = 5 + 3
product_result = 5 * 3
difference_result = 5 - 3

print("5 + 3 =", sum_result)
print("5 * 3 =", product_result)
print("5 - 3 =", difference_result)

print("Script finished running.")
